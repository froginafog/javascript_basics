//What comes after double slashes is a comment.
/*What comes between slash-star and star-slash is also a comment.*/
//To run this program, open the "index.html" file with a web browser.

document.body.getElementsByTagName("h1")[0].innerHTML = "Hello Frog";  //To print "Hello Frog" on the webpage on the first <h1> tag.
document.body.getElementsByTagName("p")[0].innerHTML = "Bye Frog";     //To print "Bye Frog" on the webpage on the first <p> tag.
document.body.getElementsByTagName("h1")[1].innerHTML = "Hello Bob";   //To print "Hello Bob" on the webpage on the second <h1> tag.
document.body.getElementsByTagName("p")[1].innerHTML = "Bye Bob";      //To print "Bye Bob" on the webpage on the second <p> tag.

console.log("Hello Frog");  //To print "Hello Frog" on the console.
console.log("Bye Frog");    //To print "Bye Frog" on the console.
console.log("Hello Bob");   //To print "Hello Bob" on the console.
console.log("Bye Bob");     //To print "Bye Bob" on the console.

