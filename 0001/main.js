document.body.getElementsByTagName("p")[0].innerHTML = "Bye Frog";     //To print "Bye Frog" on the webpage on the first <p> tag.
document.body.getElementsByTagName("h1")[1].innerHTML = "Hello Bob";   //To print "Hello Bob" on the webpage on the second <h1> tag.
